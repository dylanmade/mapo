package com.mapo.data.db

import android.content.Context
import androidx.room.Room
import androidx.test.core.app.ApplicationProvider
import androidx.test.ext.junit.runners.AndroidJUnit4
import com.mapo.data.model.Profile
import kotlinx.coroutines.flow.first
import kotlinx.coroutines.test.runTest
import org.junit.After
import org.junit.Assert.assertEquals
import org.junit.Assert.assertNotNull
import org.junit.Assert.assertNull
import org.junit.Assert.assertTrue
import org.junit.Before
import org.junit.Test
import org.junit.runner.RunWith

@RunWith(AndroidJUnit4::class)
class ProfileDaoTest {

    private lateinit var db: AppDatabase
    private lateinit var dao: ProfileDao

    @Before
    fun setUp() {
        val ctx = ApplicationProvider.getApplicationContext<Context>()
        db = Room.inMemoryDatabaseBuilder(ctx, AppDatabase::class.java)
            .allowMainThreadQueries()
            .build()
        dao = db.profileDao()
    }

    @After
    fun tearDown() {
        db.close()
    }

    @Test
    fun insert_assignsAutoGeneratedId() = runTest {
        val id = dao.insert(Profile(name = "Test"))
        assertTrue("auto-generated id should be > 0", id > 0)
    }

    @Test
    fun getById_returnsInserted() = runTest {
        val id = dao.insert(Profile(name = "Test"))
        assertEquals(Profile(id = id, name = "Test"), dao.getById(id))
    }

    @Test
    fun getById_missing_returnsNull() = runTest {
        assertNull(dao.getById(9999L))
    }

    @Test
    fun getAll_emitsAlphabeticallyOrdered() = runTest {
        dao.insert(Profile(name = "Charlie"))
        dao.insert(Profile(name = "Alpha"))
        dao.insert(Profile(name = "Bravo"))

        val names = dao.getAll().first().map { it.name }
        assertEquals(listOf("Alpha", "Bravo", "Charlie"), names)
    }

    @Test
    fun getDefault_returnsProfileFlaggedDefault() = runTest {
        dao.insert(Profile(name = "Other"))
        dao.insert(Profile(name = "DefaultProfile", isDefault = true))

        val default = dao.getDefault().first()
        assertNotNull(default)
        assertEquals("DefaultProfile", default!!.name)
    }

    @Test
    fun delete_removesProfile() = runTest {
        val id = dao.insert(Profile(name = "ToDelete"))
        val profile = dao.getById(id)!!
        dao.delete(profile)
        assertNull(dao.getById(id))
    }
}
